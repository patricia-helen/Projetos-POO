package mathtests;


public class MathTests {

    
    public static void main(String[] args) {
         // Teste do método Math.abs()
        System.out.println("Valor absoluto de -5: " + Math.abs(-5));
        
        // Teste do método Math.sqrt()
        System.out.println("Raiz quadrada de 16: " + Math.sqrt(16));
        
        // Teste do método Math.pow()
        System.out.println("2 elevado a 3: " + Math.pow(2, 3));
        
        // Teste do método Math.max()
        System.out.println("Maior valor entre 4 e 7: " + Math.max(4, 7));
        
        // Teste do método Math.min()
        System.out.println("Menor valor entre 4 e 7: " + Math.min(4, 7));
        
        // Teste do método Math.round()
        System.out.println("Arredondamento de 3.5: " + Math.round(3.5));
    }
    
}
